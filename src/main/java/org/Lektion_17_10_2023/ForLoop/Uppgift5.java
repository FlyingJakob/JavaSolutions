package org.Lektion_17_10_2023.ForLoop;

public class Uppgift5 {

    public static void main(String[] args) {
        int N = 10;

        for (int i = 0; i <= N; i++) {
            for (int j = 0; j <= N; j++) {
                System.out.println(i +"*"+j+" = "+i*j);
            }
        }


    }
}
